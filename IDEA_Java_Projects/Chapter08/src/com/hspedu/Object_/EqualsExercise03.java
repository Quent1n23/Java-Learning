package com.hspedu.Object_;

public class EqualsExercise03 {
    public static void main(String[] args) {
        /**
         * char也是基本数据类型
         * 基本数据类型 使用== 比较的是值
         * A 的 Ascii码 就是 65
         */
        int n = 65;
        char ch1 = 'A';
        char ch2 = 12;
        System.out.println(ch1 == n);
        System.out.println(ch2 == 12);

    }
}
